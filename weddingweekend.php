                    <h2>Getting to Isla Mujeres</h2>

                    <div style="margin-bottom: 30px;">

                        <h3><img src="/images/plane.png" style="margin-right:5px"> Fly in to Cancun International Airport (CUN)</h3>
                        
                        <ul>
                            <li>Since you'll be traveling internationally, you'll need a valid passport. If you don't have one you can apply for one <a href="http://travel.state.gov/content/passports/english/passports/apply.html">here</a>.</li>
                            <li><strong>Arrival time</strong>: Book a flight that arrives in Cancun no later than 9:00pm so you'll have time to travel to Isla Mujeres after you land, otherwise you'll have to spend the first night in Cancun. If you plan to arrive on Saturday, May 23 and want to join us for welcome drinks at 8:30pm, plan to arrive in Cancun no later than 6:00pm (5:00pm or earlier is better).</li>
                            <li><strong>Departure time</strong>: Book a flight that departs from Cancun no earlier than 9:00am so you'll have time to travel from Isla Mujeres to the Cancun airport on the same morning. It's recommended that you arrive at the Cancun airport at least 2.5 hours before your flight home.</li>
                            <li>On the plane the flight attendant will give you two cards to fill out, a Mexican Tourist Card (FTM) and the Customs form. Present the tourist card at immigration with your passport and they will stamp your passport and return half of the form, keep this as you'll need it when you exit the country. If you lose it, they may charge you a fine. Beware, the immigration process can be long. Then pick up any checked luggage and head to customs. At customs, you will press a button, if it is green, your bags won't be searched, if it's red, they will, then you present them with your customs card.</li>
                        </ul>

                        <h3><img src="/images/shuttle.png" style="margin-right:5px"> Take a Shuttle to Puerto Juarez, around 20 minutes depending on traffic</h3>
                        
                        <ul>
                            <li>We have negotiated a shuttle from Cancun airport to the Puerto Juarez ferry dock (which takes you to Isla Mujeres) through CARM tours. The transfer is $30/person roundtrip or $15/person one way. Click <a href="http://www.cancunandrivieramaya.com/sunhorse/iris-bert.asp">here</a> to book directly through CARM.</li>
                            <li>Your transfer will be private, unless some of our wedding guests are arriving within 30 minutes of your flight in which case you will be paired with the other guests on your shuttle.</li>
                            <li>You can also pre-purchase your Ultramar ferry ticket from CARM when you book your shuttle for $15 roundtrip, which is around the same price as purchasing them at Puerto Juarez ferry dock. This will save you from having to purchase a ticket at the ferry dock.</li>
                        </ul>

                        <h3><img src="/images/ferry.png" style="margin-right:5px"> Ride the Ultramar ferry from Puerto Juarez to Isla Mujeres, around 15 minutes</h3>
                        
                        <ul>

                            <li>If you havnâ€™t already purchased your ferry ticket from CARM tours, you will need to purchase one Ultramar ferry ticket to Isla Mujeres (~$15 roundtrip) from inside the ferry office. You can pay in US dollars or pesos.</li>
                            <li>If you want to get pesos (Mexican currency), we recommend using the ATM at the XOXO minimarket at the ferry dock to take out pesos. You can also purchase snacks and beverages at the XOXO minimarket to enjoy on your ferry ride.</li>
                            <li>Ferries run every 30 minutes 5:30am â€“ 8:30pm and hourly on the half hour after that until 11:30pm. No ferries run after 11:30pm.</li>
                            <li>Bellman may offer to help you load your luggage on the ferry, if you use their assistance, it's customary to tip them $1/luggage.</li>
                        </ul>

                        <h3><img src="/images/taxi.png" style="margin-right:5px"> Take a taxi to your hotel, around 5 minutes</h3>

                        <ul>
                            <li>When you arrive on Isla Mujeres, you can get a taxi from the taxi stand outside the ferry dock, or hail a taxi on the street.</li>
                            <li>A taxi should cost $2 - $4 US dollars or ~40 pesos to a hotel in the downtown area. Be sure to agree on a price before you enter the taxi.</li>
                            <li>If you have a lot of luggage, it is customary to tip the driver the help with your bags.</li>
                        </ul>

