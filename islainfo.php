                <h2>About Isla Mujeres</h2>
                <p>Isla Mujeres, which is Spanish for "Island of Women", is a small island in the Mexican Caribbean Sea, located eight miles off of the eastern coast of the Yucatan Peninsula near Cancun Mexico. 
                <p>In Pre-Columbian times the island was sacred to the Maya goddess of childbirth and medicine, Ix Chel. When the Spanish arrived in the 16th century they named it "Isla Mujeres" because of the many images of goddesses.
            </div>
        </div>
