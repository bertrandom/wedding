            <div class="col-md-8">
                <p>Underwater contemporary museum over 500 permanent life size sculptures, not a lot of sea life, but neat sculptures. Can visit by snorkeling or diving. For snorkeling, best to combine it with a reef snorkel as well where you can see more sealife.
