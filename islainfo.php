            <div class="col-md-12">
                <h2>Getting Around</h2>
                <p><strong>By Foot</strong> - Feel free to wander.
                <p><strong>By Taxi</strong> - You can flag a taxi down when you see them drive past. Sometimes taxis will pick you up when they already have others in the car.
                <p>Average taxi prices:
                <ul>
                    <li>Taxi within downtown = <span class="amount" data-amount="40">40 pesos</span> (ferry dock to hotel)</li>
                    <li>Taxi from downtown to middle (local part of Isla) = <span class="amount" data-amount="26">26 pesos</span> (Chedraui grocery store, Mango Café, Lolo Lorena, Monchi’s, El Varadero Cubano) </li>
                    <li>Taxi from downtown to Turtle Farm / Playa Lancheros = <span class="amount" data-amount="54">54 pesos</span></li>
                    <li>Taxi from downtown to Garrafon Park (zip lining) / Spiral Island / Dolphin Discovery = <span class="amount" data-amount="78">78 pesos</span></li>
                    <li>Taxi from downtown to Punta Sur (the other end of the island) = <span class="amount" data-amount="88">88 pesos</span></li>
                </ul>
                <p><strong>By Golf Cart</strong> – Golf carts can be rented and are a super fun way to get around the island. They are normally rented for a day (8 hours), 24 hours or multiple days. <!-- A few companies that rent golf carts are: -->
            </div>
        </div>
        <div class="row">
            <div class="col-md-12">
                <h2>Tipping</h2>
                <p>Bring a few single American dollars to use for tips on your way to Isla. The standard tip for restaurants and tours is 15%. Taxis don't expect tips unless they help you with your bags.
            </div>
        </div>
        <div class="row">
            <div class="col-md-12">
                <h2>Water</h2>
                <p>Don’t drink the water. It’s not safe to drink. Buy bottled water or drink the bottled water provided at the hotel. We recommend brushing your teeth with bottled water as well. Ice is safe to drink. The food across the island should be safe to eat. We ate at several little local restaurants as well as several street food stands without any trouble when we visited. However, please use your best judgment.
            </div>
        </div>
        <div class="row">
            <div class="col-md-12">
                <h2>Safety</h2>
                <p>Isla is very safe. There aren't any "bad" parts of the island, so feel free to explore, but keep your wits about you and again use your best judgment. The buddy system is recommended after dark. 
            </div>
        </div>
        <div class="row">
            <div class="col-md-12">
                <h2>Packing List</h2>
                <ul>
                    <li><a href="http://www.cancunmap.com/islamujeres-map.html">Isla Mujeres map</a></li>
                    <li>Beach bag or backpack</li>
                    <li>Eco-safe, biodegradable sunscreen (very expensive on Isla Mujeres)</li>
                    <li>Lip balm (high spf)</li>
                    <li>Aloe gel for sunburn</li>
                    <li>Dramamine or Bonine, if you generally get sea sick</li>
                    <li>Alka Seltzer, Excedrin, Tylenol or your favorite hangover cure</li>
                    <li>Tums, Pepto- Bismo, Imodium AD</li>
                    <li>Your favorite soap (the hotels provide very cheap soaps)</li>
                    <li>Nail kit in case of breaks/repairs</li>
                    <li>Any hair accessories you will need to do your hair for the events</li>
                    <li>Zip Loc bags in various sizes for wet bathing suits, lotions, etc</li>
                    <li>Collapsible cooler (you can buy a Styrofoam cooler on the island at 7-11)</li>
                    <li>Can Coozies - keep your drink cold</li>
                    <li>Travel alarm clock</li>
                    <li>Camera/underwater camera, film and batteries plus battery charger if necessary</li>
                    <li>Hat, cap or visor and sunglasses <li>Inexpensive raft and/or water toys</li>
                    <li>Comfortable walking shoes (if you plan to go to the Mayan Ruins or any Mainland Excursions)</li>
                    <li>Handful of $1 &amp; $5 US bills for tipping during your trip</li>
                    <li>Dive Certification Card, if you have one</li>
                    <li>Extra empty duffle bag to bring back the goodies you buy (which you can also use as a beach bag during your stay)</li>
                    <li>3 Prong adapter, if you are bringing an appliance that needs it. All rooms will have standard 2 prong outlets.</li>
                    <li>Vitamins and prescription drugs. If possible carry a copy of your prescription in case your meds are misplaced and you need to refill your prescription at the pharmacy.</li>
                    <li>It is strongly encouraged that you make photocopies of all your important documents (passport, driver’s license, credit cards- both sides, etc) in case they are lost or stolen. Remember not to pack the copies in the same place as the originals. It is best if you trade copies with your spouse or a friend.</li>
                </ul>
