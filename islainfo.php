                <h2>Climate</h2>
                <p>Tropical, hot and humid, perfect beach weather. 
                <p><img src="/vectors/icon_1067.svg" height="32" />Average high in May - 89&deg;
                <p><img src="/vectors/icon_1067.svg" height="32" />Average low in May - 75&deg;
