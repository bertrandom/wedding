                <h2>Activities &amp; Attractions</h2>
                <h3>Beaches</h3>
                
                <h4>Playa Media Luna</h4>
            </div>
        </div>
        <div class="row">
            <div class="col-md-4">
                <img src="/images/activities/playa-media-luna.jpg" class="img-thumbnail img-responsive" alt="Avenida Hidalgo">
            </div>
            <div class="col-md-8">
                <p>This rocky beach in front of Hotel Playa Media Luna isn't good for swimming, but nice for lounging or enjoying the beach view from the pool.
            </div>
        </div>
        <div class="row">
            <div class="col-md-12">
                <h4>Playa Norte</h4>
            </div>
        </div>
        <div class="row">
            <div class="col-md-8">
                <p>Located on the north end of the island a short walk from the Playa Media Luna Hotel. The most popular public beach on Isla. The sea is calm here and the beach is shallow – you can walk 25 feet out and still be in waist deep water. Most establishments charge for chair and umbrella rentals, but you can lie down a towel anywhere. Great for watching the sunset. It is a top-optional beach. 
            </div>
            <div class="col-md-4">
                <img src="/images/activities/DSC00111.jpg" class="img-thumbnail img-responsive" alt="Avenida Hidalgo">
            </div>
        </div>
        <div class="row">
            <div class="col-md-12">


                <h4>Playa Lancheros</h4>
                <p>Another public beach located on the road running along the west side of the island heading toward the south end of the island. You'll need to take a taxi or rent a golf cart to get here. Busy on weekends. Has good food options. Tix-in-Chix fish is recommended, but order one for two people as it is quite large. Also good for watching the sunset.

                <h3>Town Attractions</h3>
                <h4>Hidalgo Street</h4>
            </div>
        </div>
        <div class="row">
            <div class="col-md-4">
                <a href="https://www.flickr.com/photos/davidstanleytravel/12736094813" title="Avenida Hidalgo by David Stanley, on Flickr"><img src="https://farm6.staticflickr.com/5542/12736094813_6066162002_b.jpg" class="img-thumbnail img-responsive" alt="Avenida Hidalgo"></a>
            </div>
            <div class="col-md-8">
                <p>This pedestrian street is in the middle of downtown and where most the tourist action on the island can be found. It’s filled with restaurants, bars, tourist vendors and street artists and definitely has a party vibe. It definitely caters to tourists, so be prepared for the touts, but the lively atmosphere can be fun at night. 
            </div>
        </div>
        <div class="row">
            <div class="col-md-12">
                <h4>Town Square</h4>
                <p>At the south end of the pedestrian street, Hidalgo. Most nights they have local food vendors and craft booths. Great place to grab some cheap local eats, like churros, tamales, and marquisitas.

                <h4>Cemetary</h4>
            </div>
        </div>
        <div class="row">
            <div class="col-md-8">
                <p>Located on Lopez Mateos in front of Juarez. Super colorful.
            </div>
            <div class="col-md-4">
                <img src="/images/activities/cemetery-pink.jpg" class="img-thumbnail img-responsive">
            </div>
        </div>
        <div class="row">
            <div class="col-md-12">

                <h4>La Tortugranja (Turtle Farm)</h4>
            </div>
        </div>
        <div class="row">
            <div class="col-md-4">
                <img src="/images/activities/farm-turtle.jpg" class="img-thumbnail img-responsive">
            </div>
            <div class="col-md-8">
                <p>Located in Sac Bajo. This farm was created to protect the sea turtles from extinction. Scientists harvest the eggs and raise the babies until they are old enough to swim and then they are released into the ocean. You can see turtles of all ages swimming in the tanks as well as some other sea life. <span class="amount" data-amount="30">30 pesos</span> entrance fee.
            </div>
        </div>
        <div class="row">
            <div class="col-md-12">

                <h4><a href="http://www.garrafon.com/">Garrafon Park</a></h4>

            </div>
        </div>
        <div class="row">
            <div class="col-md-8">
                <p>Located in Punta Sur on the south end of Isla. This is an all inclusive park that includes food, towels, and gardens.
            </div>
            <div class="col-md-4">
                <img src="/images/activities/zip_line.jpg" class="img-thumbnail img-responsive">
            </div>
        </div>
        <div class="row">
            <div class="col-md-12">



                <h4>Punta Sur (South Point)</h4>
                <p>Located in the southern tip of the island. Modern sculptures line the walking paths that lead to the beautiful rocky cliffs off the coast and what is left of the Maya ruins. Definitely worth a visit. <span class="amount" data-amount="30">30 pesos</span> entrance fee. There is also a restaurant and some tourist shops here.

                <h4>JOYSXEE Floating Bottle Island</h4>

            </div>
        </div>
        <div class="row">
            <div class="col-md-4">
                <img src="/images/activities/joysxee-floating-bottle.jpg" class="img-thumbnail img-responsive">
            </div>
            <div class="col-md-8">
                <p>An island made of recycled materials. Possibly can go on a tour if the owner is around.
            </div>
        </div>
        <div class="row">
            <div class="col-md-12">



                <h3>Nearby Snorkeling Spots</h3>

                <h4>El Farito (The Lighthouse)</h4>
                <p>A very short boat ride from Isla. The boat will drop you off and then the current will guide you along the reef where you can see coral, sea fans, massive rock formations and a large variety of colorful reef fish. Then the boat will pick you up at the end of the reef near the lighthouse. You can hire a boat from the dock to take you on this trip.

                <h4>Isla Contoy</h4>

            </div>
        </div>
        <div class="row">
            <div class="col-md-8">
                <p>A 45 minute boat ride away. This island is a bird sanctuary and trips to the island feature several pretty snorkeling spots where you can see colorful coral, sting rays, have a BBQ lunch on the beach and bird watch. Tours cost around <span class="amountUSD" data-amount="60">$60</span>.
            </div>
            <div class="col-md-4">
                <img src="/images/activities/contoy.jpg" class="img-thumbnail img-responsive">
            </div>
        </div>
        <div class="row">
            <div class="col-md-12">


